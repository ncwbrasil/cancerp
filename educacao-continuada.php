	  		<p>Atelectasia é uma das mais frequentes imagens radiológicas na apresentação do câncer de pulmão. Embora não seja comumente observada nas formas iniciais da doença, é imagem frequentemente encontrada durante a sua evolução natural.

			<p>Em geral, a atelectasia surge em decorrência de obstrução parcial ou total da luz de um brônquio pelo contínuo crescimento exofítico do tumor desde seu local de origem, na mucosa brônquica, para o interior do espaço aéreo. Essa forma de atelectasia obstrutiva é denominada intrínseca de origem parietal. Em algumas circunstâncias, a obstrução brônquica intrínseca pode ser provocada pela presença intraluminar de corpos estranhos como rolhas de secreção, coágulos sanguíneos e produtos de necrose tumoral. Nesses casos, essa forma de atelectasia obstrutiva de natureza intrínseca é de origem luminar. Outra forma de atelectasia obstrutiva é aquela provocada pelo colabamento de um brônquio pela compressão extrínseca de sua luz exercida tanto pelo próprio tumor quanto por linfonodomegalia adjacente. Esse tipo de atelectasia obstrutiva é denominada extrínseca.

			<p>A persistência da obstrução de um brônquio e a consequente absorção do ar intraluminar e alveolar promove progressiva redução do volume da área pulmonar afetada provocando adaptação volumétrica da caixa torácica e das estruturas intratorácicas, promovendo desvio ipsilateral da traquéia e do mediastino além de elevação da cúpula frênica homolateral.
