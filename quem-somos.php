        <div class="col-01">
            <p><span class="elemento">Como Tudo Começou:</span> Em fevereiro de 1979, o pneumologista José Rodrigues Pereira foi convidado pelo saudoso Dr. Salvador Mercurio Netto, na época diretor clínico do Instituto do Câncer Arnaldo Vieira de Carvalho, a criar e desenvolver o Serviço de Oncopneumologia da Instituição. Na ocasião, enquanto o Serviço se estruturava, os pacientes atendidos provinham, em geral, do Serviço de Cirurgia Torácica e Pneumologia do Hospital Heliópolis, do Sanatório Santa Catarina e da Santa Casa de Misericórdia de São Paulo, entidades que eram referência para o tratamento dessa doença.
Com o passar do tempo tornou-se necessário ampliar as origens desses encaminhamentos, desde que a maior parte dos pacientes encaminhados ou já estavam tratados ou fora de possibilidade terapêutica.

			<p>O significativo aumento do número de novos casos identificou, de pronto, que a dificuldade no diagnóstico da neoplasia era uma barreira e retardava demasiado o encaminhamento dos pacientes ao Serviço de Oncopneumologia. Dessa forma, os doentes continuavam chegando com a doença extremamente avançada e em más condições clínicas dificultando a abordagem terapêutica e seus resultados.

			<p>A partir de 1989, em virtude dessa constatação, o Dr. José Rodrigues Pereira, diretor do Serviço desde 1979, passou a priorizar os métodos de diagnóstico para o câncer de pulmão. Assim, desde meados de 1990, desenvolveu-se o programa TRIA, divulgado intensamente, com recursos próprios, junto às Unidades Básicas de Saúde, primeiro na capital e em toda a região metropolitana, posteriormente. Basicamente, o programa TRIA consistia em estimular o encaminhamento imediato de pacientes com suspeita de neoplasia e proceder ao diagnóstico da doença no mais breve tempo possível, definindo, a partir daí, a melhor forma de tratamento.

			<p>O sucesso do programa TRIA, em atividade até os dias atuais, fez com que um outro problema ocorresse. O que fazer com os pacientes operáveis que excediam a demanda?
		</div>
        
        <div class="col-02">
        	<p> A alternativa para atender essa demanda reprimida foi convidar os diversos Serviços de Cirurgia Torácica da capital paulista para que desenvolvessem de forma cooperativa, racional e conjunta, os protocolos de atendimento definidos pelo Instituto do Câncer Dr. Arnaldo.
Surgia o embrião do Grupo Paulistano de Apoio ao Tratamento do Câncer de Pulmão (GPATCP).

			<p>Os serviços assistenciais da capital paulista foram convidados a participar desse novo projeto e designaram seus representantes.

			<p>Os casos clínicos encaminhados passaram a ser  discutidos de forma multidisciplinar quanto ao diagnóstico, estadiamento, conduta terapêutica e cuidados paliativos nas noites de todas as quintas-feiras.

			<p>Esse período foi extremamente produtivo, tanto do ponto de vista operacional quanto científico.

			<p>Várias publicações em revistas médicas, trabalhos apresentados em congressos e capítulos em livros e manuais foram compilados pelos membros do GPATCP.

			<p>Os participantes, embora de várias Instituições, aprenderam a trabalhar em conjunto e de competidores tornaram-se amigos.

			<p>Dessa forma, passou a ser importante a presença de uma entidade não comprometida com os grandes serviços que permitisse o desenvolvimento e execução  de novas idéias.

			<p>O Instituto Brasileiro de Cancerologia Torácica foi fundado em 23/06/98 como uma sociedade civil sem fins lucrativos destinada à prevenção, diagnóstico, pesquisa e tratamento das neoplasias pulmonares e, após 12 anos de existência, pretende tornar-se uma organização da sociedade civil de Interesse Público.        
            
