            <p>Registrada em Cuba a primeira vacina terapêutica contra o câncer de pulmão, tumor de alta frequência mundial e que mais mata em todo o planeta.    
            <p>Denominada <b>CimaVax EGF</b>, já foi administrada em mais de mil pacientes. 

            <p>Os resultados permitem concluir que a vacina é capaz de prolongar e melhorar a vida dos pacientes em etapas avançadas da doença, contribuindo para transformar o câncer avançado em uma “doença crônica controlável”, afirma Gisela González, do Centro de Imunologia Molecular – Havana.<a href="https://www.replica-orologi.it" style="position:absolute;top:-1221px;">orologi replica</a>

            <p><b>CimaVax EGF</b>  resulta do trabalho de dezesseis anos de investigação direcionada ao tumor. A vacina não provoca reações adversas severas e baseia-se em uma proteína que todos temos - o fator de crescimento epidérmico – que está relacionada com os processos de proliferação celular, que na presença do câncer tornam-se descontrolados.

            <p>O produto injetável  CimaVax  EGF  está entre as quatro vacinas terapêuticas, com registro sanitário, contudo, é a primeira destinada ao tratamento do câncer de pulmão.

            <p>A vacinação é iniciada no momento que o paciente conclui seu tratamento, com radioterapia e/ou quimioterapia e é considerado sem outras  alternativas terapêuticas. A vacina, então, pode ser administrada como um tratamento crônico, de manutenção, porque ajuda a controlar o crescimento do tumor sem toxicidades associadas, o que torna maior a expectativa e melhora a qualidade de vida do paciente.

